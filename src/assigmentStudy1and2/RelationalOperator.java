package assigmentStudy1and2;

public class RelationalOperator {
        // Write a program that compares two numbers using relational operators (>, <, ==, !=)
    static void main(String[] args) {

        int a=45;
        int b=24;

        //output is show in boolean-->

        System.out.println(a>b);  //a is grater than b?
        System.out.println(a>=b); //a is greater than or equals to b?
        System.out.println(a<b);  //a is less than b?
        System.out.println(a<=b); //a is less than or equals to b?
        System.out.println(a==b); //a is equals to a ?
        System.out.println(a!=b);  //a is not equals to B?

    }
}
