package assigmentStudy1and2;

public class DataType {
    static void main(String[] args) {

        byte rollNumber;
        rollNumber=3;
        System.out.println(rollNumber);

        short rollNumber1;
        rollNumber1=13;
        System.out.println(rollNumber1);

        int rollNumber2;
        rollNumber2=456;
        System.out.println(rollNumber2);

        long rollNumber3;
        rollNumber3=4567885;
        System.out.println(rollNumber3);

        float weight;
        weight=58.9f;
        System.out.println(weight);

        double weight2;
        weight2 = 45.2581d;
        System.out.println(weight2);

        char div;
        div='A';
        System.out.println(div);

        boolean result;  //true is pass false is fail
        result=true;
        System.out.println(result);
    }
}
