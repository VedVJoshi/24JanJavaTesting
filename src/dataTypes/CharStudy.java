package dataTypes;

public class CharStudy {
    public static void main(String[] args) {
        //I want to store division
        char division;            //variable declaration
        division = 'A';             //variable assignment / initialization
        System.out.println("My division is " + division);

        //I want to store Symbol value
        char Symbol;              //variable declaration
        Symbol = '@';               //variable assignment / initialization
        System.out.println("My Symbol is " + Symbol);

        //I want to store grade
        char grade;             //variable declaration
        grade = 'C';              //variable assignment
        System.out.println("My grade is " + grade);
    }


}
